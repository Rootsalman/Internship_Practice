import java.util.HashSet;

class HashsetDemo {
  public static void main(String args[]) {
    HashSet<String> dd = new HashSet<String>();
    System.out.println("The fruits are :");
    dd.add("Cherry");
    dd.add("Banana");
    dd.add("peach");
    dd.add("Orange");
    System.out.println(dd);
  }
}